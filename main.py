def worst_bid_price(ob_row, remaining_inventory, levels):
    """
    Prix du dernier niveau bid nécessaire pour liquider remaining_inventory.
    """
    cum_vol = 0.0
    last_price = None

    for lvl in range(levels):
        price = ob_row[2 + 4*lvl] / 10000.0   # bid price
        size  = ob_row[3 + 4*lvl]            # bid size
        cum_vol += size
        last_price = price
        if cum_vol >= remaining_inventory:
            return price

    # Pas assez de liquidité visible → pire bid observable
    return last_price

