def estimate_ac_parameters(orderbook, levels=10):

    ask = orderbook.iloc[:,0] / 10000.0
    bid = orderbook.iloc[:,2] / 10000.0

    mid = 0.5 * (ask + bid)

    returns = np.diff(mid)

    sigma = np.std(returns)

    spread = np.mean(ask - bid)

    tau = 1

    total_volume = 0

    for l in range(levels):

        ask_vol = orderbook.iloc[:,4*l + 1]
        bid_vol = orderbook.iloc[:,4*l + 3]

        total_volume += ask_vol + bid_vol

    volume = np.mean(total_volume)

    eta = spread / (0.01 * volume)

    gamma = spread / (0.1 * volume)



    return sigma, eta, gamma, tau

class AlmgrenChriss1D:
    def __init__(self, lambd, sigma, eta, gamma, tau):
        # Initialize Parameters
        self._lambda = lambd
        self._sigma = sigma
        #self._epsilon = epsilon
        self._eta = eta
        self._gamma = gamma
        self._tau = tau

        self._eta_tilda = self._eta - 0.5*self._gamma*self._tau

        # Ensure Quadratic (for optimization)
        #assert self._eta_tilda > 0

        self._kappa_tilda_squared = (self._lambda*self._sigma**2)/self._eta_tilda
        #print(f'kappa_tilda_squared = {self._kappa_tilda_squared}')

        self._kappa = np.arccosh(0.5*self._kappa_tilda_squared*self._tau**2 + 1)/self._tau
        #print(f'kappa = {self._kappa}')

    def trajectory(self, X, T):
        # Optimal Liquidation Trajectory
        ans = []
        for t in range(T):
            #x = int(np.sinh(self._kappa*(T - t))/np.sinh(self._kappa*T)*X)
            if self._kappa == 0:
              x = X* (1 - t/T)
            else:
              x = np.sinh(self._kappa*(T - t))/np.sinh(self._kappa*T)*X
            #print(f'x = {x}')
            ans.append(x)
        ans.append(0)
        return np.array(ans)

    def strategy(self, X, T):
        # Optimal Liquidation Trade List
        return np.diff(self.trajectory(X,T))

def run_ac_episode(env, volumes):

    obs = env.reset()

    done = False

    rewards = 0

    actions = []

    inventory_path = [env.remaining_inventory]

    t = 0

    while not done:

        remaining = env.remaining_inventory

        #if remaining <= 0:
        #    break

        v = volumes[t]

        action = min(1.0, v / remaining)

        obs, reward, done, info = env.step([action])

        rewards += reward

        actions.append(info["executed"])

        inventory_path.append(env.remaining_inventory)

        t += 1

    return rewards, actions, inventory_path

